#include "main.h"

/**
 * add - calculates the sum
 * @a: first integer
 * @b: second integer
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - calculates the difference
 * @a: first integer
 * @b: second integer
 *
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - calculates the product
 * @a: first integer
 * @b: second integer
 *
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - calculates the division
 * @a: first integer
 * @b: second integer
 *
 * Return: result of the division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates modulo
 * @a: first integer
 * @b: second integer
 *
 * Return: modulo val
 */
int mod(int a, int b)
{
	return (a % b);
}
